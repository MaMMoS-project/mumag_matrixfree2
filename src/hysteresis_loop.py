def _field_values(H_start: float, H_end: float, dH: float, loop: bool) -> np.ndarray:
    """Generate the sequence of field magnitudes for the sweep.

    Args:
        H_start (float): Starting field.
        H_end (float): Final field for the first pass.
        dH (float): Field step size.
        loop (bool): If True, append the return path to H_start.

    Returns:
        np.ndarray: Array of field magnitudes.
    """
    if dH == 0:
        raise ValueError("dH must be nonzero")
    if (H_end - H_start) * dH < 0:
        dH = -dH
    n = int(np.floor((H_end - H_start) / dH + 1e-12))
    vals_up = H_start + dH * np.arange(n + 1)
    if abs(vals_up[-1] - H_end) > 1e-12 * max(1.0, abs(H_end)):
        vals_up = np.append(vals_up, H_end)
    if not loop:
        return vals_up
    return np.concatenate([vals_up, vals_up[-2::-1]])


@jax.jit
def jax_compute_volume_averaged_J_parallel(
    m_nodes: jnp.ndarray,
    conn: jnp.ndarray,
    volume: jnp.ndarray,
    mat_id: jnp.ndarray,
    Js_lookup: jnp.ndarray,
    h_dir: jnp.ndarray,
) -> jnp.ndarray:
    """Compute volume-averaged magnetic polarization parallel to h_dir (JAX version).

    Args:
        m_nodes (jnp.ndarray): Nodal unit magnetization vectors.
        conn (jnp.ndarray): Tetrahedron connectivity.
        volume (jnp.ndarray): Element volumes.
        mat_id (jnp.ndarray): Element material IDs.
        Js_lookup (jnp.ndarray): Saturation polarization table.
        h_dir (jnp.ndarray): Field direction vector.

    Returns:
        jnp.ndarray: Scalar averaged polarization component.
    """
    h = h_dir / (jnp.linalg.norm(h_dir) + 1e-30)

    # Average m over tets
    m_e = m_nodes[conn]  # (E, 4, 3)
    m_avg = jnp.mean(m_e, axis=1)  # (E, 3)

    # Material properties
    Js_e = Js_lookup[mat_id - 1]  # (E,)
    J_e = Js_e[:, None] * m_avg  # (E, 3)

    # Magnetic volume (only where Js > 0)
    Vmag = jnp.sum(jnp.where(Js_e > 0, volume, 0.0)) + 1e-30

    # Volume average
    J_avg = jnp.sum(volume[:, None] * J_e, axis=0) / Vmag
    return jnp.dot(J_avg, h)


def jax_compute_volume_averaged_m(
    m_nodes: jnp.ndarray,
    conn: jnp.ndarray,
    volume: jnp.ndarray,
    mat_id: jnp.ndarray,
    Js_lookup: jnp.ndarray,
) -> jnp.ndarray:
    """Compute volume-averaged magnetization components (mx, my, mz) (JAX version).

    Only averages over magnetic material regions (Js > 0).

    Args:
        m_nodes (jnp.ndarray): Nodal unit magnetization vectors.
        conn (jnp.ndarray): Tetrahedron connectivity.
        volume (jnp.ndarray): Element volumes.
        mat_id (jnp.ndarray): Element material IDs.
        Js_lookup (jnp.ndarray): Saturation polarization table.

    Returns:
        jnp.ndarray: Average unit vector (3,).
    """
    # Average m over tets
    m_e = m_nodes[conn]  # (E, 4, 3)
    m_avg = jnp.mean(m_e, axis=1)  # (E, 3)

    # Material properties: only average over magnetic parts
    Js_e = Js_lookup[mat_id - 1]  # (E,)
    Vmag = jnp.sum(jnp.where(Js_e > 0, volume, 0.0)) + 1e-30

    m_vol_avg = (
        jnp.sum(jnp.where(Js_e[:, None] > 0, volume[:, None] * m_avg, 0.0), axis=0)
        / Vmag
    )
    return m_vol_avg


def run_hysteresis_loop(
    *,
    points: np.ndarray,
    geom: TetGeom,
    A_lookup: np.ndarray,
    K1_lookup: np.ndarray,
    K1p_lookup: np.ndarray,
    Js_lookup: np.ndarray,
    axes_lookup: np.ndarray,
    m0: np.ndarray,
    params: LoopParams,
    V_mag: float,
    node_volumes: jnp.ndarray,
    M_nodal: jnp.ndarray,
    precond_type: str = "jacobi",
    order: int = 3,
    energy_assembly: str = "segment_sum",
    grad_backend: GradBackend = "stored_grad_phi",
    chunk_elems: int = 200_000,
    cg_maxiter: int = 400,
    cg_tol: float = 1e-8,
    poisson_reg: float = 1e-12,
    boundary_mask: jnp.ndarray | None = None,
) -> dict[str, Any]:
    """Execute the full hysteresis loop simulation.

    Args:
        points (np.ndarray): Mesh node coordinates.
        geom (TetGeom): Geometry data.
        A_lookup (np.ndarray): Exchange constants.
        K1_lookup (np.ndarray): Anisotropy constants.
        K1p_lookup (np.ndarray): Orthorhombic anisotropy contribution.
        Js_lookup (np.ndarray): Saturation polarization constants.
        axes_lookup (np.ndarray): 3x3 rotation matrices per material group.
        m0 (np.ndarray): Initial magnetization state.
        params (LoopParams): Loop control parameters.
        V_mag (float): Total magnetic volume.
        node_volumes (jnp.ndarray): Lumped nodal volumes.
        M_nodal (jnp.ndarray): Nodal magnetic moments.
        precond_type (str, optional): Poisson solver preconditioner.
            Defaults to 'jacobi'.
        order (int, optional): Chebyshev order. Defaults to 3.
        energy_assembly (str, optional): assembly method. Defaults to 'segment_sum'.
        grad_backend (GradBackend, optional): Strategy for gradients.
            Defaults to 'stored_grad_phi'.
        chunk_elems (int, optional): Loop chunk size. Defaults to 200_000.
        cg_maxiter (int, optional): Solver iterations. Defaults to 400.
        cg_tol (float, optional): Solver tolerance. Defaults to 1e-8.
        poisson_reg (float, optional): Tikhonov regularization. Defaults to 1e-12.
        boundary_mask (jnp.ndarray | None, optional): Dirichlet mask.
            Defaults to None.

    Returns:
        dict[str, Any]: Results dictionary containing 'last_m', 'last_U', and 'history'.
    """
    out_dir = ensure_dir(params.out_dir)
    csv_path = out_dir / params.csv_name
    write_hysteresis_header(csv_path)

    h = np.asarray(params.h_dir, dtype=np.float64)
    h /= np.linalg.norm(h) + 1e-30

    minimize = make_minimizer(
        geom,
        A_lookup=jnp.asarray(A_lookup, dtype=jnp.float64),
        K1_lookup=jnp.asarray(K1_lookup, dtype=jnp.float64),
        Js_lookup=jnp.asarray(Js_lookup, dtype=jnp.float64),
        axes_lookup=jnp.asarray(axes_lookup, dtype=jnp.float64),
        V_mag=V_mag,
        node_volumes=node_volumes,
        M_nodal=M_nodal,
        K1p_lookup=jnp.asarray(K1p_lookup, dtype=jnp.float64),
        precond_type=precond_type,
        order=order,
        chunk_elems=chunk_elems,
        energy_assembly=energy_assembly,
        cg_maxiter=params.cg_maxiter,
        cg_tol=cg_tol,
        poisson_reg=poisson_reg,
        grad_backend=grad_backend,
        boundary_mask=boundary_mask,
    )

    m = jnp.asarray(m0, dtype=jnp.float64)
    m = m / jnp.linalg.norm(m, axis=1, keepdims=True)

    B_vals = _field_values(params.B_start, params.B_end, params.dB, params.loop)

    total_time = 0.0
    U = None
    history = []
    for step_idx, Bmag in enumerate(B_vals):
        B_ext = jnp.asarray(Bmag * h, dtype=jnp.float64)

        start_step = time.time()
        m, U, info = minimize(
            m,
            B_ext,
            U0=U,
            gamma=params.gamma,
            max_iter=params.max_iter,
            tau_f=params.tau_f,
            eps_a=params.eps_a,
            tau0=params.tau0,
            tau_min=params.tau_min,
            tau_max=params.tau_max,
            ls_eta1=params.ls_eta1,
            ls_eta2=params.ls_eta2,
            ls_C=params.ls_C,
            ls_c=params.ls_c,
            ls_s0=params.ls_s0,
            ls_max_evals=params.ls_max_evals,
            verbose=params.verbose,
        )
        # Accurate timing: wait for GPU to finish
        m.block_until_ready()
        U.block_until_ready()
        step_duration = time.time() - start_step
        total_time += step_duration

        # Compute volume averages
        Jpar = jax_compute_volume_averaged_J_parallel(
            m,
            geom.conn,
            geom.volume,
            geom.mat_id,
            jnp.asarray(Js_lookup),
            jnp.asarray(h),
        )
        m_avg = jax_compute_volume_averaged_m(
            m, geom.conn, geom.volume, geom.mat_id, jnp.asarray(Js_lookup)
        )

        B_tesla = float(Bmag) * params.Js_ref
        J_tesla = float(Jpar) * params.Js_ref
        mx, my, mz = map(float, m_avg)
        E_si = float(info.get("E", np.nan)) * (params.Js_ref**2) / (2.0 * 4e-7 * np.pi)

        # Record history row: (B_ext_T, J_par_T, mx, my, mz, E_si)
        history.append([B_tesla, J_tesla, mx, my, mz, E_si])

        append_hysteresis_row(
            csv_path,
            B_tesla,
            J_tesla,
            float(info.get("E", np.nan)),
            float(info.get("gnorm", np.nan)),
        )

        if params.snapshot_every > 0 and (step_idx % params.snapshot_every == 0):
            vtu_path = out_dir / f"state_{step_idx:05d}_B{Bmag:+.6e}.vtu"
            # m_np is only needed for VTU, we move it inside the if
            m_np = np.array(m)
            write_vtu_tetra(
                vtu_path,
                points,
                np.array(geom.conn),
                point_data={
                    "m": m_np.astype(np.float32),
                    "U": np.array(U).astype(np.float32),
                },
                cell_data={"mat_id": np.array(geom.mat_id).astype(np.int32)},
            )

        print(
            f"step {step_idx:05d}  B={B_tesla:+.6e} T  J_par={J_tesla:+.6e} T  "
            f"E={info.get('E', float('nan')):.6e}  t={step_duration:.3f}s  "
            f"it={info.get('iters', 0):.0f}  "
            f"t/it={step_duration / max(1.0, info.get('iters', 1.0)):.3e}s"
        )

    print(f"\nHysteresis loop finished in {total_time:.3f} s.")
    return {
        "out_dir": str(out_dir),
        "csv_path": str(csv_path),
        "last_m": np.array(m),
        "last_U": np.array(U),
        "history": np.array(history),
    }
